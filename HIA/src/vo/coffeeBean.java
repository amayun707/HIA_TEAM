package vo;

public class coffeeBean {
	private int coffee_num;
	private String coffee_name;
	private int cafe_num;
	private String coffee_category;
	private int price;
	private String hot_ice;
	private String coffee_info;
	private String coffee_file;
	
	public int getCoffee_num() {
		return coffee_num;
	}
	public void setCoffee_num(int coffee_num) {
		this.coffee_num = coffee_num;
	}
	public String getCoffee_name() {
		return coffee_name;
	}
	public void setCoffee_name(String coffee_name) {
		this.coffee_name = coffee_name;
	}
	public int getCafe_num() {
		return cafe_num;
	}
	public void setCafe_num(int cafe_num) {
		this.cafe_num = cafe_num;
	}
	public String getCoffee_category() {
		return coffee_category;
	}
	public void setCoffee_category(String coffee_category) {
		this.coffee_category = coffee_category;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getHot_ice() {
		return hot_ice;
	}
	public void setHot_ice(String hot_ice) {
		this.hot_ice = hot_ice;
	}
	public String getCoffee_info() {
		return coffee_info;
	}
	public void setCoffee_info(String coffee_info) {
		this.coffee_info = coffee_info;
	}
	public String getCoffee_file() {
		return coffee_file;
	}
	public void setCoffee_file(String coffee_file) {
		this.coffee_file = coffee_file;
	}
}
