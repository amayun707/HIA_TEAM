package vo;

public class cafeBean {
	
	private int cafe_num;
	private String cafe_name;
	private String id;
	private String cafe_info;
	private String cafe_location;
	private String openTime;
	private double rating;
	
	public int getCafe_num() {
		return cafe_num;
	}
	public void setCafe_num(int cafe_num) {
		this.cafe_num = cafe_num;
	}
	public String getCafe_name() {
		return cafe_name;
	}
	public void setCafe_name(String cafe_name) {
		this.cafe_name = cafe_name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCafe_info() {
		return cafe_info;
	}
	public void setCafe_info(String cafe_info) {
		this.cafe_info = cafe_info;
	}
	public String getCafe_location() {
		return cafe_location;
	}
	public void setCafe_location(String cafe_location) {
		this.cafe_location = cafe_location;
	}
	public String getOpenTime() {
		return openTime;
	}
	public void setOpenTime(String openTime) {
		this.openTime = openTime;
	}
	public double getRating() {
		return rating;
	}
	public void setRating(double rating) {
		this.rating = rating;
	}
}
